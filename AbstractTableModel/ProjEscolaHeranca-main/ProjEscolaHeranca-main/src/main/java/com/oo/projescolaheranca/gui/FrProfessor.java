    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
         Professor professorNovo = this.camposParaObjeto();
         
        if(this.editando){
           gerenciadorProfessores.atualizarProfessor(idAntigo, professorNovo);
        }else{ 
            gerenciadorProfessores.adicionarProfessor(professorNovo);
        }
        
        //limpando os botoes
        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;

        //salvando a lista no arquivo texto
        this.atualizarTabela();
    }//GEN-LAST:event_btnSalvarActionPerformed

