    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        Medico m = this.camposParaObjeto();
        String CrmAntigo = m.getCrm();

        if(this.editando){
            gerenciadorMedico.buscarMedico(CrmAntigo);
        }else{
            gerenciadorMedico.addMedico(m);
        }

        
        //No final reset a tela
        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;

        String listagem = gerenciadorMedico.toString();
        txtSelectMed.setText(listagem);
        
        gerenciadorMedico.salvarNoArquivo("ListagemMedicos.xml");
        this.habilitarCampos(true);
    }//GEN-LAST:event_btnSelectActionPerformed

    

