    private void btnCadMedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadMedActionPerformed
        Medico novoMedico = this.camposParaObjeto();

        if (this.editando) {
            this.gerenciadorMedicos.atualizarMedico(cpfEscolhido, novoMedico);
        } else { //Estou inserindo
            this.gerenciadorMedicos.addMedico(novoMedico);
        }

        this.limparCampos();
        this.habilitarCampos(false);

        String listagem = gerenciadorMedicos.toString();
        txtMedCads.setText(listagem);

        this.gerenciadorMedicos.salvarNoArquivo("ListagemMedicos.xml");
    }//GEN-LAST:event_btnCadMedActionPerformed

