    public void habilitarCampos(boolean flag) {
        txtNomeMed.setEnabled(flag);
        txtHorarioL.setEnabled(flag);
        txtEspecialidade.setEnabled(flag);
        txtCRMcad.setEnabled(flag);
    }

    public void limparCampos() {
        txtNomeMed.setText("");
        txtHorarioL.setText("");
        txtEspecialidade.setText("");
        txtCRMcad.setText("");
    }

    public void objetoParaCampos(Medico m) {
        txtNomeMed.setText(m.getNome());
        txtHorarioL.setText(m.getHorariosDisponiveis() + "");
        txtEspecialidade.setText(m.getEspecialidade() + "");
        txtCRMcad.setText(m.getCrm());
    }

    public Medico camposParaObjeto() {
        Medico m = new Medico();

        m.setNome(txtNomeMed.getText());

        m.setHorariosDisponiveis(txtHorarioL.getText());

        m.setEspecialidade(txtEspecialidade.getText());

        m.setCrm(txtCRMcad.getText());

        return m;
    }

